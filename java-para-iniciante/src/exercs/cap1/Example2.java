public class Example2 {
    public static void main(String[] args) {
        // Declaração das variáveis
        int var1;
        int var2;

        // Esta instrução atribui o valor 1024 à variável var1
        var1 = 1024;
        System.out.println("Contém o valor de var1: " + var1);

        var2 = var1 / 2;

        System.out.println("Contém o valor de < var1 / 2 >: " + var2);
    }
}
